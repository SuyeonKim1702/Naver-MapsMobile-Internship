import UIKit

struct TabViewFrame {
    var originX: CGFloat
    var originY: CGFloat
    var width: CGFloat
    var height: CGFloat

    init(originX: CGFloat, originY: CGFloat, width: CGFloat, height: CGFloat) {
        self.originX = originX
        self.originY = originY
        self.width = width
        self.height = height
    }
}
